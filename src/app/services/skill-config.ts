// 'impact'    — el sprite aparece directamente sobre el enemigo
// 'projectile' — el sprite viaja desde el jugador hasta el enemigo
// 'buff'       — se aplica sobre el jugador: animación self + buffo activo
export type SkillEffectType = 'impact' | 'projectile' | 'buff' | 'dash';

export type SkillTarget = 'enemy' | 'self';

export interface BuffDef {
  stat: string;
  value: number;
}

export interface SkillConfig {
  abilityId: string;
  effectType: SkillEffectType;
  target?: SkillTarget;   // 'enemy' (por defecto) | 'self' — habilidades sobre el propio jugador
  damage: number;
  range: number;        // tiles
  cooldown: number;     // ms
  spriteKey: string;
  frameCount: number;
  frameRate: number;
  scale: number;
  iconPath?: string;    // imagen para el botón del footer
  speed?: number;       // world pixels/second — solo para 'projectile'
  aoeRadius?: number;   // tiles — si se define, daña a todos los enemigos en este radio alrededor del impacto
  buff?: BuffDef;       // si se define, aplica este buffo al jugador al activar la habilidad
  manaCost?: number;   // mana consumido al activar (0 = gratis)
  playerAnim?: boolean; // si true, al lanzar la skill el personaje reproduce su animación de ataque (melee)
  projectileOnce?: boolean; // proyectil: animación una sola vez; el viaje no dura más que la animación
}

